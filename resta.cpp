#include <iostream>
using namespace std;

int main() {
    float num1, num2, resta;

    cout << "Ingresa el primer numero: ";
    cin >> num1;

    cout << "Ingresa el segundo numero: ";
    cin >> num2;

    resta = num1 - num2;

    cout << "La resta de " << num1 << " y " << num2 << " es: " << resta << endl;

    return 0;
}
